package Anomaly.model;

public class AnomalyRec {

	int uscd; 
	int temp;
	int humidity;
	
	
	
	
	public int getUscd() {
		return uscd;
	}
	public void setUscd(int uscd) {
		this.uscd = uscd;
	}
	public int getTemp() {
		return temp;
	}
	public void setTemp(int temp) {
		this.temp = temp;
	}
	public int getHumidity() {
		return humidity;
	}
	public void setHumidity(int humidity) {
		this.humidity = humidity;
	}


	
	
}
